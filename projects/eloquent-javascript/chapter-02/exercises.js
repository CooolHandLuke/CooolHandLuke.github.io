function drawChessboard(num) {
var board = "";
//loop starting at 0 until y is equal to the num arg
for (var y = 0; y < num; y++) {
  for (var x = 0; x < num; x++) {
//if the sum of x and y has a remainder of 0 from 2, and a space to the board string
//else add a #
    if ((x + y) % 2 == 0)
      board += " ";
    else
      board += "#";
  }
  board += "\n";
}

console.log(board);
}

